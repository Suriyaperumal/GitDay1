// String Methods

// Length : Returns the length of the String.
// toUpperCase() : converts the entire string to uppercase.
// toLowerCase() : converts the entire string to Lowercase.
// charAt(index) : Returns the character at the specified index.
// indexOf(Substring) : Returns the index of the first occurence of the specified substring.
// LastIndexOf(SubString) : Returns the index of the last occurence of the specified substring.
// includes(SubString) : checks if the string contains the specified substring.
// slice(start,end) : Extracts a section of the string(similar to substring).
// replace(oldvalue,newvalue) : Replaces the Specified value with a new value.
// trim() : Removes whitespace from both ends of the string.
// split(delimiter) : split a string into an array of substring.
// concat() : Joins two or more settings.
// StartsWith() : Checks if the string starts with the specified substring.
// endsWith() : Checks if the string ends with the specified substring.